    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        if (userName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter user name!");
        } else {
            //checking if id is exist
            try {
                //connect db
                con = Connect.connect();
                String checkName = "SELECT username FROM users WHERE username='" + userName.getText() + "'";
                pst = con.prepareStatement(checkName);
                result = pst.executeQuery();

                if (!result.next()) {
                    JOptionPane.showMessageDialog(null, "User name not exist!");
                    return;
                }

                //confirm delete
                confirmDelete = JOptionPane.showConfirmDialog(null, "Are you sure?", "Confirm", JOptionPane.YES_NO_OPTION);

                if (confirmDelete == 0) {

                    String deleteStaff = "DELETE FROM users WHERE username='" + userName.getText() + "' ";
                    pst = con.prepareStatement(deleteStaff);
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "User seccessfuly deleted!!");

                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(DeleteUser.class.getName()).log(Level.SEVERE, null, ex);
            }
            reset();
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

