    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        String name = userNametxt.getText();
        String pass = userPassword.getText();
        String position = specialization.getSelectedItem().toString();
        
        if (name.equals("") || pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter your User Name and Password!");
            return;
        } else if (position.equals("Choose")) {
            JOptionPane.showMessageDialog(null, "Please choose your position!");
            return;
        }
        try {
            con = Connect.connect();
            st = con.createStatement();
            result = st.executeQuery("SELECT * FROM users where username='" + name + "' AND userpass='" + pass + "' AND position='" + position + "' ");
            if (!result.next()) {

                JOptionPane.showMessageDialog(null, "User name or password or Position is wrong! Please check again!");
                return;
            } else {

                
                st = con.createStatement();
                result = st.executeQuery("SELECT * FROM users where username='" + name + "' AND userpass='" + pass + "' AND position='" + position + "' ");

                if (result.next()) {
                    if (position.equals("Admin")) {
                        this.setVisible(false);
                        Home home = new Home();
                        home.setVisible(true);
                    } else if (position.equals("Reception")) {
                        this.setVisible(false);
                        Home home = new Home();
                        home.setVisible(true);
                        home.admin.setEnabled(false);
                        home.teachers.setEnabled(false);
                    } else {
                        this.setVisible(false);
                        Home home = new Home();
                        home.setVisible(true);
                        home.admin.setEnabled(false);
                        home.reception.setEnabled(false);
                    }

                }
            }
        } catch (ClassNotFoundException | SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }


    }//GEN-LAST:event_loginButtonActionPerformed

